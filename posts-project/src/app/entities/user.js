
class User {
    constructor(name, city, id, username, email, phone, street, zipcode, companyName, slogan) {
        this.name = name;
        this.city = city;
        this.id = id;
        this.username = username;
        this.email = email;
        this.phone = phone;
        this.street = street;
        this.zipcode = zipcode;
        this.companyName = companyName;
        this.slogan = slogan;
    }
}

export default User